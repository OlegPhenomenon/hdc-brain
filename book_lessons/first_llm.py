def generate(model, start, length=10):
    result = [start]
    current = start

    for _ in range(length):
        if current not in model:
            break

        options = model[current]
        total = sum(options.values())

        r = random.random() * total
        cumulative = 0

        for word, count in options.items():
            cumulative += count

            if r <= cumulative:
                result.append(word)
                current = word
                break

    return " ".join(result)

